package Controller;

public abstract class ViolaoPrototype {
  public abstract String getAcustica();

  public abstract void setAcustica(String acustica);

  public abstract int getCorda();

  public abstract void setCorda(int corda);

  public abstract double getCusto();

  public abstract void setCusto(double custo);

  public abstract String getEstrutura();

  public abstract void setEstrutura(String estrutura);

  public abstract String getModelo();

  public abstract void setModelo(String modelo);

  public abstract String getNivel();

  public abstract void setNivel(String nivel);

  public abstract String getTipoCorda();

  public abstract void setTipoCorda(String tipoCorda);
}
